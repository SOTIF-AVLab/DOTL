function [X,Y] = node(P1,P2,Q1,Q2)
if (P1(1)==P2(1))
    X=P1(1);
    k2=(Q2(2)-Q1(2))/(Q2(1)-Q1(1));
    b2=Q1(2)-k2*Q1(1);
    Y=k2*X+b2;
elseif (Q1(1)==Q2(1))
    X=Q1(1);
    k1=(P2(2)-P1(2))/(P2(1)-P1(1));
    b1=P1(2)-k1*P1(1);
    Y=k1*X+b1;
else
    k1=(P2(2)-P1(2))/(P2(1)-P1(1));
    k2=(Q2(2)-Q1(2))/(Q2(1)-Q1(1));
    b1=P1(2)-k1*P1(1);
    b2=Q1(2)-k2*Q1(1);
    X=(b2-b1)/(k1-k2);
    Y=k1*X+b1;
end
end